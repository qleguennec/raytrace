#include "rt.h"

t_v3
	apply_mat
	(float *mat
	, t_v3 a)
{
	return (V3(a.x * mat[0] + a.y * mat[1] + a.z * mat[2]
		, a.x * mat[3] + a.y * mat[4] + a.z * mat[5]
		, a.x * mat[6] + a.y * mat[7] + a.z * mat[8]));
}
